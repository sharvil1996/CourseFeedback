package courseFeedback.bean;

public class DateFeedbackCounterBean {

	private String dateFeedbackCounterId;
	private String date;
	private String counter;
	
	public String getDateFeedbackCounterId() {
		return dateFeedbackCounterId;
	}
	public void setDateFeedbackCounterId(String dateFeedbackCounterId) {
		this.dateFeedbackCounterId = dateFeedbackCounterId;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getCounter() {
		return counter;
	}
	public void setCounter(String counter) {
		this.counter = counter;
	}
}
